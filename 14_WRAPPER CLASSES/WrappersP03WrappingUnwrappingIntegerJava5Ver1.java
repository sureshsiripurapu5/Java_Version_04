class WrappersP03WrappingUnwrappingIntegerJava5Ver1
{
    public static void main(String[ ] args)
    { 
	   // Auto Boxing==>primitive TO object from Ver 5
       int x=23;
	   Integer  intObj=x;   
	   System.out.println("value of intObj="+intObj);
	
	   // Auto UnBoxing==>object TO primitive from Ver5
	    int i=intObj;
		System.out.println("value of i="+i);
	}
}

 